export function getPersonalityDialogues(personality: 'gentle' | 'coach' | 'hongkong') {
  switch (personality) {
    case 'gentle':
      return gentleNutritionistDialogues;
    case 'coach':
      return strictCoachDialogues;
    case 'hongkong':
      return hongKongCoachDialogues;
    default:
      return gentleNutritionistDialogues;
  }
}

export function getRandomDialogue(personality: 'gentle' | 'coach' | 'hongkong'): string {
  const dialogues = getPersonalityDialogues(personality);
  return dialogues[Math.floor(Math.random() * dialogues.length)];
}
